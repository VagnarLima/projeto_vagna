package ifba.webII;

public class Ator {

	
	private int id;
	private String nome;

	public Ator() {
	}

	public Ator(String nome) {
		this.nome = nome;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	
	
	
}
